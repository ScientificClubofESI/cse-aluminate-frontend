import Head from 'next/head';

const SEO = ({ 
  title = 'CSE Aluminate - Connect with ESI Alumni',
  description = 'Discover and connect with ESI alumni professionals. Find expert services, skills, and talent across various industries.',
  keywords = 'ESI alumni, computer science alumni, engineering graduates, professional network',
  ogImage = '/SEOpage.png',
  ogUrl = 'https://cse-aluminate.cse.club',
  canonical,
  structuredData,
}) => {
  const fullTitle = title.includes('CSE Aluminate') ? title : `${title} | CSE Aluminate`;
  const canonicalUrl = canonical || ogUrl;

  return (
    <Head>
      {/* Primary Meta Tags */}
      <title>{fullTitle}</title>
      <meta name="title" content={fullTitle} />
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      
      {/* Open Graph / Facebook */}
      <meta property="og:type" content="website" />
      <meta property="og:url" content={ogUrl} />
      <meta property="og:title" content={fullTitle} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={ogImage} />
      <meta property="og:image:alt" content={fullTitle} />
      <meta property="og:image:width" content="1200" />
      <meta property="og:image:height" content="630" />
      <meta property="og:site_name" content="CSE Aluminate" />
      
      {/* Twitter */}
      <meta name="twitter:card" content="summary_large_image" />
      <meta name="twitter:url" content={ogUrl} />
      <meta name="twitter:title" content={fullTitle} />
      <meta name="twitter:description" content={description} />
      <meta name="twitter:image" content={ogImage} />
      <meta name="twitter:image:alt" content={fullTitle} />
      
      {/* Canonical URL */}
      <link rel="canonical" href={canonicalUrl} />
      
      {/* Structured Data */}
      {structuredData && (
        <script
          type="application/ld+json"
          dangerouslySetInnerHTML={{ __html: JSON.stringify(structuredData) }}
        />
      )}
    </Head>
  );
};

export default SEO;
